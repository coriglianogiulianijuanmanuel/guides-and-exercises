"use strict";
// The result of a % b is the remainder of the integer division of a by b.
console.log(2.5 % 1.5); // 1

// The exponentiation operator is defined for non-integer numbers as well.
// For example, a square root is an exponentiation by 1/2:
console.log(81 ** (1 / 2)); // 9
// And a cubic root is an exponentiation by 1/3:
console.log(8 ** (1 / 3)); // 2
