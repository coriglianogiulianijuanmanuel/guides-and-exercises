"use strict";

// JavaScript has a built-in Math object which contains a small library of mathematical functions and constants.

// Math.random()
// Returns a random number from 0 to 1 (not including 1).
console.log(Math.random());

// Math.max(a, b, c...)
// Returns the greatest from the arbitrary number of arguments.
console.log(Math.max(3, 5, -10, 0, 1)); // 5

// Math.min(a, b, c...)
// Returns the smallest from the arbitrary number of arguments.
console.log(Math.min(3, 5, -10, 0, 1)); // -10

// Math.pow(n, power)
// Returns n raised to the given power.
console.log(Math.pow(2, 3)); // 8 (2 in power 3 = 8)
